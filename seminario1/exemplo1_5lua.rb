# Algoritmo de amostragem de falta de ortogonalidade 3
# Regra: Em Ruby, é possível inserir o valor de uma variável
#        dentro de uma string. Em outras palavras, uma variável pode
#        ser interpolada em uma string, permitindo que seu conteúdo
#        seja exibido diretamente no texto.
# Exceção: Exceto, se a string for representada por aspas simples (‘ ‘)
# Teste: Testar interpolação com variável global.

$nome = 'Luciana'
puts "Bom dia, #{$nome}."
puts 'Bom dia, #{$nome}.'
